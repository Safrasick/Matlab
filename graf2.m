% Задаем значение параметра b
b = 3;
% Задаем диапазон для t с шагом pi/12
t = 0:pi/12:2*pi;
% Вычисляем x(t) и y(t)
x = b * cos(t).^3;
y = b * sin(t).^3;
% Строим график
figure;
plot(x, y, '-o');  % Строим график с метками для точек
grid on;           % Включаем сетку
title('Параметрический график x(t) и y(t)');
xlabel('x(t)');
ylabel('y(t)');
axis equal;        % Устанавливаем одинаковые масштабы по осям
